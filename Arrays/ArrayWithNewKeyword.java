class ArrayWithNewKeyword{
	public static void main(String[] args){
		//Array using new keyword.
		
		//declaratin
		int[] arr;
		
		//creation or instantiation
		
		arr=new int[4];// size is mandatory to mention while creating an array with new keyword.
	
		//initilization
			
		arr[0]=10;
		arr[1]=20;
		arr[2]=30;
		arr[3]=40;
		System.out.println(arr);//address or hexadecimal value.
				
		System.out.println(arr[0]);		
	}
}